# Simple Python program for basic mathematical operations

# Define two numbers
num1 = 10
num2 = 5

# Addition
addition = num1 + num2
print(f"Addition: {num1} + {num2} = {addition}")

# Subtraction
subtraction = num1 - num2
print(f"Subtraction: {num1} - {num2} = {subtraction}")

# Multiplication
multiplication = num1 * num2
print(f"Multiplication: {num1} * {num2} = {multiplication}")

# Division
division = num1 / num2
print(f"Division: {num1} / {num2} = {division}")

# Power (Exponentiation)
power = num1 ** num2
print(f"Power: {num1} raised to the power of {num2} = {power}")
